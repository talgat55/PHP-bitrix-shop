		<h3> НАШИ ПРИНЦИПЫ – ЭКОЛОГИЧНСТЬ И ДОСТУПНОСТЬ</h3>
		 Первостепенное значение ГК «Зеленый сад» уделяет экологичности строящегося жилья. Наша компания – первый и единственный из рязанских застройщиков, который является членом российского Совета по экологическому строительству. Весь процесс строительства – начиная от выбора места расположения жилого комплекса и заканчивая применяемыми стройматериалами и инженерными решениями – подчинен экологическим стандартам и задаче формирования благоприятной среды для здоровья жильцов. <br>
 <br>
		 Один из важнейших принципов компании также состоит в том, чтобы комфортное жилье было доступным. «Зеленый сад» постоянно разрабатывает и внедряет все новые механизмы, облегчающие процесс приобретения жилья и позволяющие улучшить жилищные условия даже людям с невысоким уровнем дохода. Сегодня наши клиенты могут воспользоваться субсидированной ипотекой по беспрецедентно низким ставкам, рассрочкой платежей, программой Trade-in по обмену старого жилья на новое, бонусными накопительным программами и другими возможностями.
		<h3>ОТВЕТСТВЕННОСТЬ НА ВЕСЬ СРОК ЭКСПЛУАТАЦИИ</h3>
		 В состав группы компаний входит управляющая жилищная компания «Зеленый сад – Мой дом», которая по выбору собственников принимает дома на обслуживание после их ввода в эксплуатацию. По сути, мы несем пожизненную ответственность за построенное жилье, руководствуясь при эксплуатации такими же высокими стандартами, которые ставим себе при строительстве жилья. Подтверждением этой планки служат «Знак качества ЖКХ» и звание «Дом образцового содержания», которые управляющая компания и один из обслуживаемых домов получили в феврале 2018 года. Это первый и пока единственный в Рязани случай присуждения таких наград. <br>
