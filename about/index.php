		 Группа компаний «Зеленый сад» работает в строительной сфере Рязани с 2000 года и входит в число крупнейших застройщиков, сформировавших современный облик Рязани. Основное направление деятельности — проектирование и строительство современных жилых комплексов. Наши проекты отличаются стильной архитектурой, экологичностью применяемых материалов, «умной» инженерией и продуманной инфраструктурой. Мы не просто строим квартиры – мы создаем полноценное, комфортное и безопасное жилое пространство. Стратегия ГК «Зеленый сад» – экологичное жилье для гармоничной жизни.
		<h3>СТРОИТЕЛЬНАЯ КОМПАНИЯ В РЯЗАНИ «ЗЕЛЕНЫЙ САД»: НАШИ ДОСТИЖЕНИЯ</h3>
		 Группа компаний – это комплекс лицензированных проектных и строительных предприятий Рязани, отлично зарекомендовавших себя на рынке жилой недвижимости. Общий штат превышает 500 квалифицированных сотрудников — наши кадры действительно решают все! Компания и ее сотрудники регулярно заносятся на Доску Почета Рязанской области, региональную Доску почета строителей, отмечаются правительственными и отраслевыми наградами. Мы гордимся результатами своего труда – на сегодня это 27 объектов многоквартирной жилой застройки. В построенных нами домах живут без малого 15 000 человек! <br>
 <br>
		 Кроме того, ГК «Зеленый сад» занимается возведением масштабных социальных объектов по поручению Правительства Рязанской области. Профессионализм и добросовестность нашей команды сыграли решающую роль в досрочном открытии Академии тенниса им. Николая Озерова, второй ледовой арены Дворца спорта «Олимпийский», спорткомплекса в городе Михайлове. В 2017 году компания построила передовой для города детский центр на 250 мест, где созданы все условия для занятий, в том числе, детей с ограниченными возможностями здоровья.
		<h3>«ЗЕЛЕНЫЙ САД» - ЭТО ПЕРЕДОВЫЕ РАЗРАБОТКИ ДЛЯ КОМФОРТА И БЕЗОПАНОСТИ</h3>
		 Мы ведем постоянный мониторинг пожеланий наших клиентов и передовых тенденций в строительной отрасли. Благодаря этому «Зеленый сад» регулярно совершенствует технологии строительства и внедряет уникальные для рязанского рынка разработки. Сегодня мы применяем в наших проектах такие решения, как:
